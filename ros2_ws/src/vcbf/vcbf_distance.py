def predict_trained_gan_grayscale(depth):
    """""
    This function gets depth as input and outputs the predicted CBF generated by Neural Network. The CBF image returned from this function is between -50 and 50 currently.
    """""
    global model, count

    depth = np.asarray(depth)
    depth = np.uint8(depth)
    depth = cv2.resize(depth, (256,256))
    depth = np.asarray(depth)
    depth = depth.reshape((1,256,256,1))
    depth = (depth - 127.5) / 127.5
    gen_cbf = model.predict(depth, verbose = 0)
    gen_cbf = resize(gen_cbf, [IM_HEIGHT,IM_WIDTH])

    OldValue = gen_cbf
    OldMin = -1
    NewMax = 255
    NewMin = 0
    OldMax = 1
    scaled_CBF = (((OldValue - OldMin) * (NewMax - NewMin)) / (OldMax - OldMin)) + NewMin
    scaled_CBF = np.uint8(scaled_CBF)
    scaled_CBF = scaled_CBF.reshape(IM_HEIGHT,IM_WIDTH)

    gen_cbf = scaled_CBF
    gen_cbf = (gen_cbf - 127.5) / 127.5
    gen_cbf = 50 * gen_cbf
    edges_y, edges_x = np.gradient(gen_cbf)

    return gen_cbf,  edges_x, edges_y

def distance_transform(depth):

    depth = np.asarray(depth)
    depth_not = 255 - depth
    depth = np.uint8(depth)
    depth_not = np.uint8(depth_not)
    result = depth.copy()
    result = cv2.distanceTransform(result, distanceType=cv2.DIST_L2, maskSize=3, dstType=cv2.CV_8UC1)
    result_not = depth_not.copy()
    result_not = cv2.distanceTransform(result_not, distanceType=cv2.DIST_L2, maskSize=5, dstType=cv2.CV_8UC1)
    result = -result
    final = result + result_not

    
    edges_y, edges_x = np.gradient(final)

    return final, edges_x, edges_y

