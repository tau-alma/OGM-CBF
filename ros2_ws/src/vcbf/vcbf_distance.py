def depth_to_array(image, camera_num = 1):

    """
    Convert an image containing CARLA encoded depth-map to a 2D array containing
    the depth value of each pixel normalized between [0.0, 1.0] and then multiplied by 1000.
    Based on camera number final depth image will be restored in it's corresponding global variable to be used later
    """

    global depth_image, depth_image2, depth_image3
    image.convert(carla.ColorConverter.Depth)
    
    array = to_bgra_array(image)
    array = array.astype(np.float32)
    # # Apply (R + G * 256 + B * 256 * 256) / (256 * 256 * 256 - 1).
    normalized_depth = np.dot(array[:, :, :3], [65536.0, 256.0, 1.0])
    normalized_depth /= 16777215.0  # (256.0 * 256.0 * 256.0 - 1.0)
    normalized_depth = 1000 * normalized_depth
   
    ### thresholding : depth values further that 20 will be removed
    normalized_depth[normalized_depth>20] = 255
    normalized_depth = np.asarray(normalized_depth)

    ### storing depth image in coressponding global variable depend on which camera is calling it
    
    if camera_num == 1:
        depth_image = normalized_depth

    elif camera_num == 2:
        depth_image2 = normalized_depth

    else:
        depth_image3 = normalized_depth
        
    
    return depth_image

def to_bgra_array(image):
    """Convert a CARLA raw image to a BGRA numpy array."""

    array = np.frombuffer(image.raw_data, dtype=np.dtype("uint8"))
    array = np.reshape(array, (image.height, image.width, 4))
    return array

def to_rgb_array(image):
    """Convert a CARLA raw image to a RGB numpy array."""
    array = to_bgra_array(image)
    # Convert BGRA to RGB.
    array = array[:, :, :3]
    array = array[:, :, ::-1]
    return array

